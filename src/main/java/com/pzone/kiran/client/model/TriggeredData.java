package com.pzone.kiran.client.model;

public class TriggeredData {
	private String pzoneCode;
	private String amtoteCode;
	private String timezone;
	private String localDate;
	private String country;
	private String eventType;
	private String raceNo;
	private String postTime;
	private String serverTime;

	public String getPzoneCode() {
		return pzoneCode;
	}

	public void setPzoneCode(String pzoneCode) {
		this.pzoneCode = pzoneCode;
	}

	public String getAmtoteCode() {
		return amtoteCode;
	}

	public void setAmtoteCode(String amtoteCode) {
		this.amtoteCode = amtoteCode;
	}

	public String getTimezone() {
		return timezone;
	}

	public void setTimezone(String timezone) {
		this.timezone = timezone;
	}

	public String getLocalDate() {
		return localDate;
	}

	public void setLocalDate(String localDate) {
		this.localDate = localDate;
	}

	public String getCountry() {
		return country;
	}

	public void setCountry(String country) {
		this.country = country;
	}

	public String getEventType() {
		return eventType;
	}

	public void setEventType(String eventType) {
		this.eventType = eventType;
	}

	public String getRaceNo() {
		return raceNo;
	}

	public void setRaceNo(String raceNo) {
		this.raceNo = raceNo;
	}

	public String getPostTime() {
		return postTime;
	}

	public void setPostTime(String postTime) {
		this.postTime = postTime;
	}

	public String getServerTime() {
		return serverTime;
	}

	public void setServerTime(String serverTime) {
		this.serverTime = serverTime;
	}

}
